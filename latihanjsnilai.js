var angka1=5
var angka2=5
var operator='%'

switch(operator){
  case '+': {console.log(angka1+angka2); break; }
  case '-': {console.log(angka1-angka2); break; }
  case '*': {console.log(angka1*angka2); break; }
  case '/': {console.log(angka1/angka2); break; }
  case '%': {console.log(angka1%angka2); break; }
  default: {console.log('tidak terjadi apa apa');}
}